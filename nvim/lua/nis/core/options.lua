local opt = vim.opt

-- appearance
opt.termguicolors = true
opt.background = 'dark'
opt.signcolumn = 'yes'
opt.cursorline = true

-- tabs
opt.tabstop = 2
opt.shiftwidth = 2
opt.expandtab = true
opt.smartindent = true

-- search
opt.ignorecase = true
opt.smartcase = true

-- line numbers
opt.number = true
opt.relativenumber = true

-- split 
opt.splitbelow = true
opt.splitright = true

-- clipboard
opt.clipboard = 'unnamed'

-- backspace
opt.backspace = 'indent,eol,start'

